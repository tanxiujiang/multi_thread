package harry.tan.thread.entity;

public class Student {
    private String name;
    private String sex;
    private int    age;



    public String getName() {
        return name;
    }



    public void setName(String pName) {
        name = pName;
    }



    public String getSex() {
        return sex;
    }



    public void setSex(String pSex) {
        sex = pSex;
    }



    public int getAge() {
        return age;
    }



    public void setAge(int pAge) {
        age = pAge;
    }

}
